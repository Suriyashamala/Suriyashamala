package day1;

public class Datatype {
    public static void main(String[] args) {
        int num1 = 7;
        float num2 = 2.748f;
        long num3 = 25396766828l;
        double num4 = 6.73864;
        char letter = 'R';
        String name = "Reshma";
        System.out.println("num1 is an integer;" + num1);
        System.out.println("num2 is a float;" + num2);
        System.out.println("num3 is a long;" + num3);
        System.out.println("num4 is a double;" + num4);
        System.out.println("letter is a char;" + letter);
        System.out.println("name is a char" + name);

    }
}